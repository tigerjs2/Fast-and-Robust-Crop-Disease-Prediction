import argparse
import sys
from pathlib import Path
from typing import Any, Dict, Optional, Sequence, Tuple

import torch


PROJECT_ROOT = Path(__file__).resolve().parents[1]
if str(PROJECT_ROOT) not in sys.path:
	sys.path.append(str(PROJECT_ROOT))

from scripts.SecondStageTrain import SecondStageClassifier


DEFAULT_CHECKPOINT_PATH = PROJECT_ROOT / "weights" / "second_stage" / "best_classifier.pth"


def _try_import_executorch_to_edge():
	"""ExecuTorch 버전 차이를 흡수해 to_edge 함수를 가져온다."""
	try:
		from executorch.exir import to_edge  # type: ignore

		return to_edge
	except Exception:
		try:
			from executorch import exir  # type: ignore

			return exir.to_edge
		except Exception as exc:
			raise ImportError(
				"Failed to import ExecuTorch export API. "
				"Install a compatible executorch package first."
			) from exc


def _default_output_path(checkpoint_path: Path) -> Path:
	return checkpoint_path.with_suffix(".pte")


def _get_num_classes(checkpoint: Dict[str, Any]) -> int:
	class_names = checkpoint.get("class_names")
	if isinstance(class_names, Sequence) and len(class_names) > 0:
		return len(class_names)

	config = checkpoint.get("config", {})
	raw_class_names = config.get("class_names")
	if isinstance(raw_class_names, str) and raw_class_names.strip():
		parsed = [x.strip() for x in raw_class_names.split(",") if x.strip()]
		if parsed:
			return len(parsed)

	raise ValueError(
		"Could not infer num_classes from checkpoint. "
		"Expected non-empty 'class_names' in checkpoint payload."
	)


def _get_embed_dim(checkpoint: Dict[str, Any], fallback: int = 512) -> int:
	config = checkpoint.get("config", {})
	if isinstance(config.get("embed_dim"), int):
		return int(config["embed_dim"])

	state_dict = checkpoint.get("model_state_dict", {})
	key = "classifier.1.weight"
	if key in state_dict and hasattr(state_dict[key], "shape") and len(state_dict[key].shape) == 2:
		return int(state_dict[key].shape[1])

	return fallback


def build_model_from_checkpoint(
	checkpoint: Dict[str, Any],
	force_backbone_weights_none: bool = True,
) -> Tuple[SecondStageClassifier, int]:
	config = checkpoint.get("config", {})
	num_classes = _get_num_classes(checkpoint)
	embed_dim = _get_embed_dim(checkpoint)

	backbone_name = str(config.get("backbone_name", "mobilenet_v3_large"))
	backbone_weights = None if force_backbone_weights_none else config.get("backbone_weights", "DEFAULT")
	num_heads = int(config.get("num_heads", 8))
	attn_dropout = float(config.get("attn_dropout", 0.0))
	residual_scale = float(config.get("residual_scale", 1.0))
	classifier_dropout = float(config.get("classifier_dropout", 0.1))

	model = SecondStageClassifier(
		num_classes=num_classes,
		backbone_name=backbone_name,
		backbone_weights=backbone_weights,
		embed_dim=embed_dim,
		num_heads=num_heads,
		attn_dropout=attn_dropout,
		residual_scale=residual_scale,
		classifier_dropout=classifier_dropout,
	)

	state_dict = checkpoint.get("model_state_dict")
	if not isinstance(state_dict, dict):
		raise ValueError("Checkpoint must contain a dict field named 'model_state_dict'.")

	model.load_state_dict(state_dict, strict=True)
	model.eval()
	return model, embed_dim


def export_pth_to_pte(
	checkpoint_path: Path,
	output_path: Path,
	image_size: int,
	num_text_queries: int,
	batch_size: int,
	strict_export: bool,
) -> None:
	checkpoint = torch.load(str(checkpoint_path), map_location="cpu")
	if not isinstance(checkpoint, dict):
		raise ValueError("Checkpoint payload must be a dictionary.")

	model, embed_dim = build_model_from_checkpoint(checkpoint)

	sample_images = torch.randn(batch_size, 3, image_size, image_size, dtype=torch.float32)
	sample_text_queries = torch.randn(batch_size, num_text_queries, embed_dim, dtype=torch.float32)

	exported_program = torch.export.export(
		model,
		(sample_images, sample_text_queries),
		strict=strict_export,
	)

	to_edge = _try_import_executorch_to_edge()
	edge_program = to_edge(exported_program)
	executorch_program = edge_program.to_executorch()

	program_buffer = getattr(executorch_program, "buffer", None)
	if program_buffer is None:
		raise RuntimeError("Failed to access ExecuTorch program buffer.")

	output_path.parent.mkdir(parents=True, exist_ok=True)
	with output_path.open("wb") as f:
		f.write(program_buffer)


def build_arg_parser() -> argparse.ArgumentParser:
	parser = argparse.ArgumentParser(
		description="Convert SecondStageTrain checkpoint (.pth) to ExecuTorch program (.pte)"
	)
	parser.add_argument(
		"--checkpoint_path",
		type=str,
		default=str(DEFAULT_CHECKPOINT_PATH),
		help="Path to .pth checkpoint generated by SecondStageTrain.py",
	)
	parser.add_argument(
		"--output_path",
		type=str,
		default=None,
		help="Output .pte path (default: same name as checkpoint with .pte)",
	)
	parser.add_argument(
		"--image_size",
		type=int,
		default=224,
		help="Dummy image size used for export tracing",
	)
	parser.add_argument(
		"--num_text_queries",
		type=int,
		default=5,
		help="Number of text queries per sample (must match runtime input contract)",
	)
	parser.add_argument(
		"--batch_size",
		type=int,
		default=1,
		help="Dummy batch size used for export tracing",
	)
	parser.add_argument(
		"--strict_export",
		action="store_true",
		help="Use strict mode in torch.export.export",
	)
	return parser


def main() -> None:
	parser = build_arg_parser()
	args = parser.parse_args()

	checkpoint_path = Path(args.checkpoint_path)
	if not checkpoint_path.exists():
		raise FileNotFoundError(f"Checkpoint not found: {checkpoint_path}")

	output_path = Path(args.output_path) if args.output_path else _default_output_path(checkpoint_path)

	export_pth_to_pte(
		checkpoint_path=checkpoint_path,
		output_path=output_path,
		image_size=args.image_size,
		num_text_queries=args.num_text_queries,
		batch_size=args.batch_size,
		strict_export=args.strict_export,
	)

	print(f"Converted checkpoint: {checkpoint_path}")
	print(f"Saved ExecuTorch program: {output_path}")


if __name__ == "__main__":
	main()
